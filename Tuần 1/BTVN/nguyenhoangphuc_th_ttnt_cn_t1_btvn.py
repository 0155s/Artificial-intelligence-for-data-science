class State:
    def __init__(self, data, g=0, h=0, parent=None):
        self.data = data
        self.g = g # Số bước đã đi
        self.h = h # Ước tính số bước còn lại
        self.f = g + h # Tổng chi phí AKT
        self.parent = parent

    def __lt__(self, other):
        return self.f < other.f

# Đếm số ô sai vị trí so với đích
def calculate_h(current, goal):
    count = 0
    for i in range(N):
        for j in range(N):
            if current[i][j] != 0 and current[i][j] != goal[i][j]:
                count += 1
    return count

def find_blank(data):
    for i in range(N):
        for j in range(N):
            if data[i][j] == 0: return i, j

def move(data, r1, c1, r2, c2):
    new_data = copy.deepcopy(data)
    new_data[r1][c1], new_data[r2][c2] = new_data[r2][c2], new_data[r1][c1]
    return new_data

def print_path(node):
    path = []
    curr = node
    while curr:
        path.append(curr.data)
        curr = curr.parent
    path.reverse()

    print(f"=== KẾT QUẢ TÌM KIẾM AKT ===")
    print(f"Tổng số bước di chuyển tối ưu: {len(path) - 1}")
    for step, matrix in enumerate(path):
        print(f"\n[Bước {step}]")
        for row in matrix:
            print(" ".join(f"{x:2}" if x != 0 else " _" for x in row))
    print("\n=> Kết luận: đã tìm ra đường đi ngắn nhất bằng các trạng thái có ít ô sai nhất.")

def solve_15_puzzle(initial_matrix):
    start_node = State(initial_matrix)
    start_node.h = calculate_h(initial_matrix, goal_state)

    open_list = PriorityQueue()
    open_list.put(start_node)
    closed_list = set()

    while not open_list.empty():
        current = open_list.get()

        if current.data == goal_state:
            return current

        state_tuple = tuple(tuple(row) for row in current.data)
        if state_tuple in closed_list: continue
        closed_list.add(state_tuple)

        r, c = find_blank(current.data)
        for dr, dc in [(-1, 0), (1, 0), (0, -1), (0, 1)]:
            nr, nc = r + dr, c + dc
            if 0 <= nr < N and 0 <= nc < N:
                child_data = move(current.data, r, c, nr, nc)
                h = calculate_h(child_data, goal_state)
                child_node = State(child_data, current.g + 1, h, current)
                open_list.put(child_node)
    return None

