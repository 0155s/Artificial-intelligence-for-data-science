import heapq

def giai_tsp_astar(matrix, start_node=0):
    n = len(matrix)

    min_edge = min(matrix[i][j] for i in range(n) for j in range(n) if i != j)

    pq = [(0, 0, start_node, 1 << start_node, [start_node])]

    visited_costs = {}

    print(f"--- BẮT ĐẦU TÌM KIẾM LỘ TRÌNH TỪ THÀNH PHỐ {start_node} ---")

    while pq:
        f, g, u, mask, path = heapq.heappop(pq)

        if mask == (1 << n) - 1:
            total_path = path + [start_node]
            total_cost = g + matrix[u][start_node]

            print("\nLỘ TRÌNH TỐI ƯU:")
            print(f"- Lộ trình di chuyển: {' -> '.join(map(str, total_path))}")
            print(f"- Tổng chi phí: {total_cost}")
            return total_path, total_cost

        state = (u, mask)
        if state in visited_costs and visited_costs[state] <= g:
            continue
        visited_costs[state] = g

        for v in range(n):
            if not (mask & (1 << v)):
                new_g = g + matrix[u][v]

                remaining_cities = n - bin(mask).count('1')
                h = remaining_cities * min_edge

                new_f = new_g + h
                heapq.heappush(pq, (new_f, new_g, v, mask | (1 << v), path + [v]))

