def a_star_search(graph, heuristics, start, goal):
    open_list = {start}
    closed_list = set()

    g = {start: 0}
    parents = {start: start}

    while open_list:
        # Chọn đỉnh n có f(n) = g(n) + h(n) nhỏ nhất
        n = min(open_list, key=lambda v: g[v] + heuristics[v])

        if n == goal:
            path = []
            while parents[n] != n:
                path.append(n)
                n = parents[n]
            path.append(start)
            path.reverse()

            print(f"=== KẾT QUẢ TÌM KIẾM A* ===")
            print(f"Đường đi tìm được: {' -> '.join(path)}")
            print(f"Tổng chi phí thực tế: {g[goal]}")
            print("\n=> Kết luận: Thuật giải A* đảm bảo tìm được đường đi ngắn nhất nhờ kết hợp giữa chi phí đã đi và ước lượng tương lai.")
            return path

        for (m, weight) in graph.get(n, []):
            if m not in open_list and m not in closed_list:
                open_list.add(m)
                parents[m] = n
                g[m] = g[n] + weight
            else:
                if g[m] > g[n] + weight:
                    g[m] = g[n] + weight
                    parents[m] = n
                    if m in closed_list:
                        closed_list.remove(m)
                        open_list.add(m)

        open_list.remove(n)
        closed_list.add(n)

    print("Không tìm thấy đường đi!")
    return None

