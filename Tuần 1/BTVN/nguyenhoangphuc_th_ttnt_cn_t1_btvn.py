def tron_mang_min(a, b):
    res = []
    n1, n2 = len(a), len(b)
    for i in range(max(n1, n2)):
        if i < n1 and i < n2:
            res.append(min(a[i], b[i]))
        elif i < n1:
            res.append(a[i])
        else:
            res.append(b[i])
    return res

