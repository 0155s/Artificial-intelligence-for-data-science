import heapq
import time

class PuzzleSolver:
    def __init__(self, n, initial_state):
        self.n = n
        self.start = initial_state
        self.goal = self._generate_goal(n)

        self.goal_pos = {self.goal[r][c]: (r, c) for r in range(n) for c in range(n)}

    def _generate_goal(self, n):
        """Tạo ma trận đích: 1, 2, 3... (n*n-1), 0"""
        flat_goal = list(range(1, n*n)) + [0]
        return [flat_goal[i:i+n] for i in range(0, len(flat_goal), n)]

    def manhattan(self, state):
        """Quy trình tính tổng khoảng cách Manhattan cho toàn bảng"""
        dist = 0
        for r in range(self.n):
            for c in range(self.n):
                val = state[r][c]
                if val != 0:
                    target_r, target_c = self.goal_pos[val]
                    dist += abs(r - target_r) + abs(c - target_c)
        return dist

    def solve(self):
        # (f, g, current_state, path_history)
        h_start = self.manhattan(self.start)
        # Lưu path dưới dạng list các ma trận
        pq = [(h_start, 0, self.start, [])]
        visited = set()

        start_time = time.time()

        while pq:
            f, g, curr, path = heapq.heappop(pq)

            if curr == self.goal:
                return path + [curr], time.time() - start_time

            state_tuple = tuple(tuple(row) for row in curr)
            if state_tuple in visited: continue
            visited.add(state_tuple)

            r, c = next((r, c) for r in range(self.n) for c in range(self.n) if curr[r][c] == 0)

            for dr, dc in [(-1, 0), (1, 0), (0, -1), (0, 1)]:
                nr, nc = r + dr, c + dc
                if 0 <= nr < self.n and 0 <= nc < self.n:

                    new_state = [list(row) for row in curr]
                    new_state[r][c], new_state[nr][nc] = new_state[nr][nc], new_state[r][c]

                    if tuple(tuple(row) for row in new_state) not in visited:
                        h = self.manhattan(new_state)
                        heapq.heappush(pq, (g + 1 + h, g + 1, new_state, path + [curr]))
        return None, 0

